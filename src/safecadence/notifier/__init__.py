"""Real notification channels — proves the alert path actually works.

We deliberately avoid any heavyweight dependencies here: a Slack incoming
webhook is just an HTTP POST with a JSON body, and we already require
``httpx`` for the [server] / [ai] extras.

If httpx isn't installed (the bare CLI install), notify() returns
``{"sent": False, "reason": "httpx not installed"}`` so callers can
degrade gracefully — never raise.
"""

from __future__ import annotations

import json
import os
from typing import Any


SEVERITY_EMOJI = {
    "critical": ":rotating_light:",
    "high": ":warning:",
    "medium": ":small_orange_diamond:",
    "low": ":small_blue_diamond:",
    "info": ":information_source:",
}


def _format_slack_blocks(events: list[dict]) -> dict:
    """Produce a Slack Block Kit payload for a batch of events."""
    if not events:
        return {"text": "SafeCadence: no new findings."}

    crit = sum(1 for e in events if (e.get("severity") or "").lower() == "critical")
    high = sum(1 for e in events if (e.get("severity") or "").lower() == "high")
    headline = (
        f":shield: SafeCadence — {len(events)} new findings"
        f" ({crit} critical, {high} high)"
    )
    blocks: list[dict] = [
        {"type": "header", "text": {"type": "plain_text", "text": headline}},
    ]
    # Show up to 10 highest-severity events; everything else is summarised.
    rank = {"critical": 0, "high": 1, "medium": 2, "low": 3, "info": 4}
    sorted_events = sorted(events,
                            key=lambda e: rank.get((e.get("severity") or "info").lower(), 5))
    for e in sorted_events[:10]:
        sev = (e.get("severity") or "info").lower()
        emoji = SEVERITY_EMOJI.get(sev, "•")
        title = e.get("title") or e.get("type") or "finding"
        asset = e.get("asset_id") or e.get("subject") or ""
        why = e.get("why") or e.get("conflict") or e.get("evidence") or ""
        if isinstance(why, dict):
            why = json.dumps(why)
        blocks.append({
            "type": "section",
            "text": {
                "type": "mrkdwn",
                "text": (f"{emoji} *[{sev.upper()}] {title}*"
                         + (f"\n  asset: `{asset}`" if asset else "")
                         + (f"\n  {why}" if why else "")),
            },
        })
    if len(sorted_events) > 10:
        blocks.append({
            "type": "context",
            "elements": [{"type": "mrkdwn",
                          "text": f"_+{len(sorted_events) - 10} more not shown — "
                                  f"see `/api/policy/compliance` for the full list._"}],
        })
    blocks.append({
        "type": "context",
        "elements": [{"type": "mrkdwn",
                      "text": "Generated by SafeCadence — local-first network "
                              "+ identity security platform."}],
    })
    return {"text": headline, "blocks": blocks}


def notify_slack(webhook_url: str, events: list[dict],
                 *, timeout: int = 10,
                 signing_secret: str | None = None) -> dict[str, Any]:
    """POST a Block Kit payload to a Slack incoming-webhook URL.

    Returns ``{"sent": bool, "status": int, "reason": str}``. Never
    raises — callers expect a quiet, graceful failure when networks
    are unavailable.
    """
    if not webhook_url:
        return {"sent": False, "reason": "no webhook url configured"}
    if not webhook_url.startswith("https://hooks.slack.com/"):
        return {"sent": False,
                "reason": "webhook url does not look like a Slack incoming webhook"}
    try:
        import httpx
    except ImportError:
        return {"sent": False, "reason": "httpx not installed (pip install "
                                          "'safecadence-netrisk[ai]' or [server])"}
    payload = _format_slack_blocks(events)
    try:
        with httpx.Client(timeout=timeout) as client:
            r = client.post(webhook_url, json=payload)
        return {
            "sent": 200 <= r.status_code < 300,
            "status": r.status_code,
            "reason": r.text[:200] if r.status_code >= 400 else "ok",
            "events_sent": len(events),
        }
    except Exception as e:
        return {"sent": False, "reason": f"{type(e).__name__}: {e}"}


def notify(webhook_url: str | None, events: list[dict],
            *, signing_secret: str | None = None) -> dict[str, Any]:
    """High-level dispatch — pick the right channel based on the URL.

    v7.2 dispatches to Slack / Microsoft Teams / PagerDuty / generic
    HMAC-signed webhook. A receiver that wants signature verification
    sets ``signing_secret`` and we compute the SHA-256 HMAC over the
    raw payload, send it in the X-SafeCadence-Signature header.
    """
    if not webhook_url:
        webhook_url = (os.environ.get("SC_SLACK_WEBHOOK")
                        or os.environ.get("SAFECADENCE_SLACK_WEBHOOK")
                        or os.environ.get("SC_WEBHOOK_URL"))
    if not webhook_url:
        return {"sent": False, "reason": "no webhook configured"}
    if "hooks.slack.com" in webhook_url:
        return notify_slack(webhook_url, events,
                              signing_secret=signing_secret)
    if "webhook.office.com" in webhook_url or "office.com/webhookb2" in webhook_url:
        return notify_teams(webhook_url, events,
                              signing_secret=signing_secret)
    if "events.pagerduty.com" in webhook_url:
        return notify_pagerduty(webhook_url, events,
                                  signing_secret=signing_secret)
    # Generic webhook — HMAC-signed JSON of the events list.
    return notify_generic(webhook_url, events,
                            signing_secret=signing_secret)


